def score_traces(traces: list[Trace], answer: str) -> None:
    """Assign binary rewards to all traces in a group (in-place).

    Extracts the model's final answer from the last chunk of each trace.
    Score is 1.0 if the answer is mathematically equivalent to `answer`, else 0.0.
    """
    for trace in traces:
        if not trace.chunks:
            trace.reward = 0.0
            continue
        # Concatenate all chunk responses to form the full trace text.
        all_ids = torch.cat([c.response_ids for c in trace.chunks])
        full_text = trace.chunks[0].input_ids.new_zeros(0)  # dummy
        # Decode the full trace response for answer extraction.
        tokenizer_ref = None  # set externally; see generate_group_traces
        trace.answer = answer
        # Reward is set inside generate_group_traces after decoding.


def compute_grpo_advantages(rewards: list[float]) -> list[float]:
    """Normalize a group of rewards into GRPO advantages: (R - mean) / std.

    If all rewards are identical (std == 0), advantages are all 0 — no gradient
    signal, which is correct (the group provides no discriminating information).
    """
    import statistics
    mean = sum(rewards) / len(rewards)
    if len(rewards) < 2:
        return [0.0] * len(rewards)
    std = statistics.stdev(rewards)
    if std < 1e-8:
        return [0.0] * len(rewards)
    return [(r - mean) / std for r in rewards]


# ---------------------------------------------------------------------------
# Loss computation
# ---------------------------------------------------------------------------

def compute_trace_loss(
    model: AutoModelForCausalLM,
    ref_model: AutoModelForCausalLM,
    trace: Trace,
    advantage: float,
    clip_eps: float = 0.2,
    kl_coef: float = 0.001,
) -> torch.Tensor:
    """Compute the Delethink policy gradient loss for one trace.

    Loss formula (Algorithm 1 / Eq. 1 from the paper):

        L(τ) = (1 / ℓ(τ)) * Σ_{(x,y) in τ} Σ_t U(x, y, t)

    where ℓ(τ) = total response tokens across all chunks, and U is the
    per-token PPO clip objective:

        U(x, y, t) = min(r_t * A, clip(r_t, 1-ε, 1+ε) * A) - β * KL_t

    with r_t = πθ(y_t) / πθ_old(y_t) and KL_t = log πθ(y_t) - log πref(y_t).

    Note: advantage A is the same for every token in every chunk of the trace
    (trace-level reward, not step-level). This matches GRPO's group normalization.

    Args:
        trace:     a Trace generated during rollout (contains old_log_probs).
        advantage: scalar GRPO advantage for this trace.
        clip_eps:  PPO clip ratio (ε). Default 0.2.
        kl_coef:   KL penalty coefficient (β). Default 0.001 (Yuan reference).

    Returns:
        Scalar loss tensor (mean over all response tokens, normalized by ℓ(τ)).
    """
    trace_len = trace.total_response_tokens
    if trace_len == 0:
        return None

    device = trace.chunks[0].response_ids.device
    total_weighted_loss = None

    for chunk in trace.chunks:
        inp = chunk.input_ids
        resp = chunk.response_ids

        # Current policy log probs (with gradient).
        curr_lp = _chunk_log_probs_grad(model, inp, resp)

        # Old policy log probs (no grad, stored at rollout time).
        old_lp = chunk.old_log_probs.detach()

        # Reference policy log probs (no grad).
        with torch.no_grad():
            ref_lp = _chunk_log_probs(ref_model, inp, resp)

        # PPO importance ratio and clipped surrogate.
        ratio = torch.exp(curr_lp - old_lp)
        clip_ratio = torch.clamp(ratio, 1.0 - clip_eps, 1.0 + clip_eps)
        ppo_term = -torch.min(ratio * advantage, clip_ratio * advantage)

        # Per-token KL divergence (forward KL proxy: log πθ - log πref).
        kl_term = curr_lp - ref_lp

        token_loss = ppo_term + kl_coef * kl_term  # shape: (resp_len,)

        # Weight by chunk length for trace-length normalization.
        chunk_contribution = token_loss.sum()
        total_weighted_loss = (
            chunk_contribution if total_weighted_loss is None
            else total_weighted_loss + chunk_contribution
        )

    if total_weighted_loss is None:
        return None
    return total_weighted_loss / trace_len


def _chunk_log_probs_grad(
    model: AutoModelForCausalLM,
    input_ids: torch.Tensor,
    response_ids: torch.Tensor,
) -> torch.Tensor:
    """Same as _chunk_log_probs but retains the computation graph for backprop."""
    full_ids = torch.cat([input_ids, response_ids]).unsqueeze(0)
    logits = model(full_ids).logits[0]

    input_len = input_ids.shape[0]
    resp_len = response_ids.shape[0]

    response_logits = logits[input_len - 1 : input_len + resp_len - 1]
    log_probs = F.log_softmax(response_logits, dim=-1)
    return log_probs[torch.arange(resp_len), response_ids]


