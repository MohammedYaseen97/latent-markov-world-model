@torch.no_grad()
def _generate_chunk(
    model: AutoModelForCausalLM,
    input_ids: torch.Tensor,
    max_new_tokens: int,
    temperature: float,
    top_p: float,
    eos_token_id: int,
) -> torch.Tensor:
    """Run one forward pass through the model and return the generated token IDs only.

    Args:
        input_ids:      1D tensor of prompt token IDs (already on model device).
        max_new_tokens: generation budget for this chunk.

    Returns:
        1D tensor of generated token IDs (not including the prompt).
    """
    input_ids_2d = input_ids.unsqueeze(0)
    attention_mask = torch.ones_like(input_ids_2d)
    out = model.generate(
        input_ids_2d,
        attention_mask=attention_mask,
        max_new_tokens=max_new_tokens,
        do_sample=True,
        temperature=temperature,
        top_p=top_p,
        pad_token_id=eos_token_id,
        eos_token_id=eos_token_id,
    )
    return out[0, input_ids.shape[0]:]  # strip prompt prefix


@torch.no_grad()
def _chunk_log_probs(
    model: AutoModelForCausalLM,
    input_ids: torch.Tensor,
    response_ids: torch.Tensor,
) -> torch.Tensor:
    """Compute per-token log probs of response_ids given input_ids context.

    Runs one forward pass over [input_ids | response_ids] and extracts the
    log probs of the response tokens — i.e. the model's probability of having
    generated each response token given the prefix.

    Returns:
        1D tensor of shape (len(response_ids),) with log probabilities.
    """
    full_ids = torch.cat([input_ids, response_ids]).unsqueeze(0)
    logits = model(full_ids).logits[0]  # (seq_len, vocab_size)

    input_len = input_ids.shape[0]
    resp_len = response_ids.shape[0]

    # logits[i] predicts the token at position i+1.
    # To predict response_ids[t], use logits[input_len + t - 1].
    response_logits = logits[input_len - 1 : input_len + resp_len - 1]
    log_probs = F.log_softmax(response_logits, dim=-1)
    return log_probs[torch.arange(resp_len), response_ids]


@torch.no_grad()
def generate_delethink_trace(
    model: AutoModelForCausalLM,
    tokenizer: AutoTokenizer,
    query_ids: list[int],
    cfg: dict[str, Any],
    temperature: float,
    top_p: float,
) -> Trace:
    """Generate one Delethink trace for a single problem.

    Implements Algorithm 1 from the Delethink paper (Aghajohari et al., ICLR 2026).
    Three phases per trace:

      Chunk 1:  input = query.
                Generate up to C tokens.
                Fold the first `planning_prefix_tokens` of the response into query
                permanently (these capture the model's initial approach).

      Chunk l≥2: input = query (with planning prefix) + last m tokens of prev chunk.
                 Generate up to C-m tokens.
                 Stop early if EOS is produced.

    All tensor operations are no-grad. Log probs of the generated tokens are
    computed separately (also no-grad) and stored in each Chunk for use during
    the policy gradient update.

    Args:
        query_ids: tokenized prompt (list of ints, not yet a tensor).
        cfg:       token_markov config block from the merged YAML.

    Returns:
        A Trace with populated chunks and reward=0.0 (reward is filled later).
    """
    C = cfg["chunk_size_tokens"]       # per-chunk generation cap
    m = cfg["max_carryover_tokens"]    # Markov state size (last m tokens)
    I = cfg["iteration_cap"]           # max chunks per trace
    P = cfg["planning_prefix_tokens"]  # first P tokens of chunk 1 folded into query
    eos = tokenizer.eos_token_id
    device = next(model.parameters()).device

    query_t = torch.tensor(query_ids, device=device, dtype=torch.long)
    prompt_t = query_t.clone()
    trace = Trace()

    for i in range(I):
        max_new = C if i == 0 else (C - m)
        response_t = _generate_chunk(model, prompt_t, max_new, temperature, top_p, eos)

        if response_t.shape[0] == 0:
            break  # model generated nothing (edge case)

        old_lp = _chunk_log_probs(model, prompt_t, response_t)
        trace.chunks.append(Chunk(
            input_ids=prompt_t.clone(),
            response_ids=response_t.clone(),
            old_log_probs=old_lp,
        ))

        # Fold first P tokens of chunk 1 into the persistent query prefix.
        if i == 0:
            planning = response_t[:P]
            query_t = torch.cat([query_t, planning])

        # Stop at EOS.
        if response_t[-1].item() == eos:
            break

        # Build next chunk's prompt: query (+ planning prefix) + Markov carryover.
        carryover = response_t[-m:]
        prompt_t = torch.cat([query_t, carryover])

    return trace


