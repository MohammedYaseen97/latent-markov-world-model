"""Token-Markov GRPO training — Delethink-style RL-learned textual carryover.

Arm 2 of the four-arm ablation. Implements the Markovian Thinking paradigm from:
  "The Markovian Thinker: Architecture-Agnostic Linear Scaling of Reasoning"
  Aghajohari et al., ICLR 2026. https://github.com/McGill-NLP/the-markovian-thinker

Design: generation proceeds in fixed-size chunks. At each chunk boundary, old
context is deleted; only the last m tokens carry forward as the Markov state.
The model learns — via GRPO reward signal — what information to preserve in
that carryover window. Context bounding is structural, not learned, so the
Markov property is guaranteed by construction.

This module deliberately does NOT use TRL's GRPOTrainer. The multi-chunk
generation loop and the trace-level loss normalization are incompatible with
TRL's single-sequence-per-rollout assumption. We implement the training loop
directly, following Algorithm 1 from the Delethink paper.

All reward and equivalence logic is shared with grpo_baseline.py per contract.
All hyperparameters (lr, batch size, seed, etc.) are inherited from
configs/train_baseline_grpo.yaml via the config extends chain.
"""
