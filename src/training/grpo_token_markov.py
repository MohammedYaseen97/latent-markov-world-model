@dataclass
class Chunk:
    """One chunk of a Delethink trace.

    input_ids:    token IDs fed as prompt for this chunk (query + carryover).
    response_ids: token IDs generated by the model for this chunk.
    old_log_probs: per-token log probs under the policy at rollout time (no grad).
                   Shape: (len(response_ids),). Used as the denominator in the
                   PPO importance ratio.
    """
    input_ids: torch.Tensor
    response_ids: torch.Tensor
    old_log_probs: torch.Tensor


@dataclass
class Trace:
    """A complete Delethink rollout for one problem — a sequence of chunks.

    chunks:  ordered list of Chunk objects (1 to iteration_cap).
    reward:  scalar reward assigned to the full trace (0.0 or 1.0).
    answer:  the ground-truth answer string for this problem.
    """
    chunks: list[Chunk] = field(default_factory=list)
    reward: float = 0.0
    answer: str = ""

    @property
    def total_response_tokens(self) -> int:
        """Total new tokens generated across all chunks — the trace length ℓ(τ)."""
        return sum(c.response_ids.shape[0] for c in self.chunks)


